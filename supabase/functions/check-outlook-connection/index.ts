
import { serve } from "https://deno.land/std@0.177.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.7.1";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    // Parse the authorization header
    const authHeader = req.headers.get('Authorization');
    if (!authHeader) {
      return new Response(
        JSON.stringify({ error: 'No authorization header' }),
        { status: 401, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    // Initialize the Supabase client
    const supabaseUrl = Deno.env.get('SUPABASE_URL') ?? '';
    const supabaseAnonKey = Deno.env.get('SUPABASE_ANON_KEY') ?? '';
    const supabase = createClient(supabaseUrl, supabaseAnonKey);

    // Verify the user
    const token = authHeader.replace('Bearer ', '');
    const { data: { user }, error: userError } = await supabase.auth.getUser(token);
    
    if (userError || !user) {
      return new Response(
        JSON.stringify({ error: 'Invalid auth token' }),
        { status: 401, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    // Check if MS_CLIENT_ID and MS_CLIENT_SECRET are configured
    const msClientId = Deno.env.get('MS_CLIENT_ID');
    const msClientSecret = Deno.env.get('MS_CLIENT_SECRET');
    const redirectUri = Deno.env.get('REDIRECT_URI');
    
    const configurationComplete = !!(msClientId && msClientSecret && redirectUri);
    
    // Check if the user has an Outlook token
    let connected = false;
    if (configurationComplete) {
      const { data: tokenData, error: tokenError } = await supabase
        .from('outlook_tokens')
        .select('access_token')
        .eq('user_id', user.id)
        .single();
      
      connected = !!tokenData && !tokenError;
    }

    return new Response(
      JSON.stringify({ 
        connected,
        configurationComplete 
      }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    return new Response(
      JSON.stringify({ error: error.message }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
